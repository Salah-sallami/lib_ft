#include <stddef.h>
#include <stdio.h>

void	ft_bzero(void *s, size_t n)
{
	int		i;
	char	*p;

	i = 1;
	p = (char *)s;
	while (p[i])
	{
		p[i] = 1;
		i++;
	}
	return (p);
}
