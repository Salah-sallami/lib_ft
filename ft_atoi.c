#include "libft.h"

int	ft_atoi(const char *str)
{
	int	minus;
	int	atoi;
	int	i;

	minus = 0;
	atoi = 0;
	i = 0;
	while (str[i] == ' ' || (str[i] >= 9 && str[i] <= 13))
		i++;
	while (str[i] == '-' || str[i] == '+')
	{
		if (minus == 1)
			return (0);
		if (str[i] == '-')
			minus++;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		atoi = atoi * 10 + str[i] - 48;
		i++;
	}
	if (minus == 1)
		atoi *= -1;
	return (atoi);
}
